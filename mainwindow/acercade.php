					<p style="color: black;font-family: 'Courier New', Courier, monospace;">
					SISTEMA PARA DETECCIÓN Y AISLAMIENTO DE FALLAS</p>
					<br/>
					<p>Actualmente existen una gran cantidad de soluciones encaminadas a detectar
						 y corregir fallas, de manera temprana, en el equipamiento activo de las redes.
						Estas funcionan solo a partir del monitoreo de la red y no están exentas de 
						generar falsos positivos o múltiples alarmas que desorientan a los administradores
						sobre el origen y la localización de las fallas. La detección tardía implica la 
						degradación de los servicios que se ofrecen provocando el incumplimiento de los 
						acuerdos de nivel de operación y de servicio. Las herramientas existentes, resultan
						insuficientes para correlacionar el impacto asociado a la ocurrencia de fallas para
						automatizar las tareas relacionadas a la solución de las mismas. El presente trabajo 
						propone un sistema para la disminuir la degradación de los servicios a partir de la 
						detección temprana de los síntomas relacionados con la ocurrencia de las fallas en el 
						equipamiento activo, minimizando el impacto en los usuarios. El sistema incorpora una 
						arquitectura de Gestión Basada en Políticas que permite la corrección automatizada de 
						las fallas disminuyendo la afectación en los servicios. Además, agiliza la localización 
						de las fallas a través de la aplicación de un algoritmo de detección de cambios al análisis 
						de parámetros en variables de las MIB del equipamiento activo de redes. El mismo permite
						el análisis panorámico de la red empleando un modelo de detección de anomalía basado en 
						grafos, que además considera, el control de los activos y la estructura de la red almacenados
						en una Base de Datos de Gestión de Configuración.</p>
					<br/>
					<p style="color: black;font-family: 'Courier New', Courier, monospace;">
					Detección de anomalías basada en aprendizaje profundo: Revisión</p>
					<br/>
					<p>La detección de anomalías es una técnica de Minería de Datos que permite el reconocimiento de 
						nuevos patrones con comportamiento inusual, los cuales pueden ser traducidos como acciones no
						válidas o anómalas sobre los datos. La detección de anomalías ha permitido la identificación
						y prevención de actividades maliciosas como fraude e intrusiones, entre otros. El uso de 
						técnicas tradicionales para la detección de anomalías ha reportado muy buenos resultados.
						Sin embargo, en los últimos años se han reportado resultados de mayor relevancia mediante 
						el uso de técnicas de aprendizaje profundo. El objetivo de este reporte es la revisión de 
						los principales y más recientes métodos del estado-del-arte para la detección de anomalías
						(fraude e intrusiones) basados en aprendizaje profundo (en inglés: Deep Learning), los 
						cuales categorizamos según el tipo de red profunda que utilizan.</p>
					<br/>
					<p style="color: black;font-family: 'Courier New', Courier, monospace;">
					Técnicas de aprendizaje automático para la detección de intrusos en redes de computadoras</p>
					<br/>
					<p>El desarrollo de sistemas de detección de intrusos en redes de computadoras (del inglés NIDS)
						 constituye un reto para los investigadores, debido a que con el crecimiento de las redes de
						computadoras, aparecen, constantemente nuevos ataques basados en contenido. El presente 
						artículo además de hacer una descripción de los enfoques de detección de intrusos basados
						en firmas y en anomalías, constituye una revisión de las diferentes técnicas de aprendizaje
						automático a aplicar en las etapas de preprocesamiento y procesamiento de los datos para 
						la detección. Se describen la taxonomía de los NIDS y un esquema de clasificación de atributos 
						de conexiones. En la detección de anomalías a partir de técnicas de aprendizaje automático 
						varios son los conjuntos de datos empleados, siendo KDD Cup 99 el más utilizado. Atendiendo 
						a esto se describe ese conjunto de datos y se exponen resultados obtenidos sobre el mismo a 
						partir de algunas técnicas de preprocesamiento de datos como selección y discretización. 
						Son expuestos novedosos enfoques que hibridan algoritmos de búsqueda basados en inteligencia 
						de enjambre con algoritmos de aprendizaje automático, lo que posibilita elevar los índices 
						de detección y mejoran la detección de ataques basados en contenido. </p>


